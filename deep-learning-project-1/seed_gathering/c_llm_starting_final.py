def unindent(s):
    lines = s.splitlines()
    non_blank_lines = [line for line in lines if line.strip()]
    min_indent = min(len(line) - len(line.lstrip())
                     for line in non_blank_lines) if non_blank_lines else 0
    unindented_lines = [line[min_indent:] if len(
        line) >= min_indent else line for line in lines]
    return '\n'.join(unindented_lines)

def c_extract_docstring(code):
    # Find the first occurrence of /* (start of the comment)
    first_comment = code.find('/*')
    if first_comment == -1:
        return "", code  # No comment found, return empty string and the original code

    # Find the last occurrence of */ (end of the comment)
    second_comment = code.find('*/', first_comment + 2)
    if second_comment == -1:
        return "", code  # No closing comment found, return empty string and the original code

    # Extract the comment between /* and */
    comment = code[first_comment + 2:second_comment]

    # Clean the comment by removing any leading or trailing spaces
    comment = comment.strip()

    # Remove the comment from the code
    code = code[:first_comment] + code[second_comment + 2:]

    return comment, code

