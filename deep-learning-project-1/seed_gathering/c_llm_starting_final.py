RETURN_QUERY = LANGUAGE.query("""
(return_statement) @return
""")

def does_have_return(src, parser=global_parser):
    """
    Check if the C code contains return statements with values.
    """
    tree = parser.parse(bytes(src, "utf8"))
    root = tree.root_node
    captures = RETURN_QUERY.captures(root)
    for node, _ in captures:
        if len(node.children) <= 1:  # Includes "return" itself
            continue
        return True
    return False

def run_clang(d):
    try:
        # Run clang with static analysis on the C files in the directory
        outs = subprocess.run(
            ["clang", "-analyze"] + [f for f in os.listdir(d) if f.endswith('.c')],
            cwd=d,
            capture_output=True,
            text=True,
            timeout=120,
        )

        return parse_clang_output(outs)

    except Exception as e:
        print(f"Error running clang: {e}")
        return {}

def parse_clang_output(outs):
    filemap = {}
    lines = outs.stdout.split("\n")

    for line in lines:
        if line.strip():
            parts = line.split(":")
            if len(parts) >= 3:  # Clang output format
                file = parts[0].split("/")[-1]  # Extract file name from path
                if file not in filemap:
                    filemap[file] = 0
                if "warning" in line or "error" in line:
                    filemap[file] += 1  # Count warnings and errors

    return filemap

def typecheck_batch(files: List[str]) -> Dict[str, str]:
    """
    Type checks a batch of C files using clang. Only files with no errors are retained.

    Args:
    - files (List[str]): List of C file contents (as strings).

    Returns:
    - Dict[str, str]: A dictionary of file hashes (unique) and the file contents
      that passed the type check.
    """
    filemap: Dict[str, str] = {}

    with tempfile.TemporaryDirectory() as tempdir:
        # Write each C file content to a temporary file
        for contents in files:
            hash_object = hashlib.sha1(bytes(contents, "utf8"))
            hex_dig = hash_object.hexdigest()
            name = os.path.join(tempdir, hex_dig + ".c")
            with open(name, "w") as f:
                f.write(contents)

        # Run clang to check for errors
        typecheck_map = run_clang(tempdir)
        print("Typecheck results:", typecheck_map)

        if not typecheck_map:
            print("No errors reported; returning all files.")
            return {hex_dig: contents for contents in files}  # Return all files if no errors

        # Remove files with errors from filemap
        for filename, errors in typecheck_map.items():
            no_c = filename.replace(".c", "")
            if errors == 0:
                continue  # No errors, retain the file
            if no_c in filemap:
                del filemap[no_c]  # Remove from valid files

        print(f"Pass rate: {len(filemap)}/{len(files)}")
        return filemap

def infer_includes(code: str) -> str:
    try:
        with tempfile.TemporaryDirectory() as tempdir:
            c_file = os.path.join(tempdir, "temp.c")
            with open(c_file, "w") as f:
                f.write(code)

            result = subprocess.run(
                ["clang", "-fsyntax-only", c_file],
                capture_output=True,
                text=True,
                timeout=10
            )

            if result.stderr:
                inferred_includes = process_clang_errors(result.stderr)
                return inferred_includes + "\n" + code

            return code

    except Exception as e:
        print(f"Error while inferring includes: {e}")
        return code

def process_clang_errors(stderr: str) -> str:
    missing_includes = []
    if "stdio.h" in stderr:
        missing_includes.append("#include <stdio.h>")
    if "stdlib.h" in stderr:
        missing_includes.append("#include <stdlib.h>")

    return "\n".join(missing_includes)

