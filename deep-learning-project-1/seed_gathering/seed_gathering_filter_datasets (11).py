import re

def pre_filtering_c(ex):
    code = ex["content"]
    code_bytes = code.encode('utf-8')

    # Filter out bad substrings
    lower = code.lower()
    for word in BAD_WORDS:
        if word in lower:
            print(f"Filtered out due to bad word: {word}")
            return False

    for bad_import in BAD_IMPORTS:
        if bad_import in code:
            print(f"Filtered out due to bad import: {bad_import}")
            return False

    # Too many lines of code (say 150)
    lines = code.split("\n")
    if len(lines) > 150:
        print("Filtered out due to too many lines")
        return False

    # Function argument check
    for line in lines:
        if re.match(r'^[a-zA-Z_]\w*\s+[a-zA-Z_]\w*\s*\(.*\)\s*{', line):
            print("Filtered out due to function without arguments")
            return False

    # Return statement check
    parser = make_parser()  # Ensure this is defined correctly
    if not does_have_return(code, parser=parser):
        print("Filtered out due to missing return statement")
        return False

    try:
        tree = global_parser.parse(code_bytes)
        captures = FN_BLOCK_QUERY.captures(tree.root_node)

        # Check if captures are found
        if not captures:
            print("Filtered out due to missing captures")
            return False

        block, _ = captures[0]

        # Check for comments as docstring (C-style comment)
        exp = block.children[0]
        if exp.type != 'comment':
            print("Filtered out due to missing comment block")
            return False

        docstring = exp.children[0]
        docstring_text = docstring.text.decode('utf-8')
        if not docstring_text.startswith("/*") and not docstring_text.endswith("*/"):
            print("Filtered out due to improperly formatted docstring")
            return False
    except Exception as e:
        print(f"Error in filtering: {e}")
        return False

    return True  # All good!

